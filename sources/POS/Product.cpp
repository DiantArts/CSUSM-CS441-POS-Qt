::pos::Product::Product(
    ::std::size_t id,
    ::std::string name,
    float price,
    ::std::size_t quantity
) noexcept
    : m_id{ id }
    , m_name{ name }
    , m_price{ price }
    , m_quantity{ quantity }
{}



///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
// Accessors
//
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
auto ::pos::Product::getId() const
    -> ::std::size_t
{
    return m_id;
}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
auto ::pos::Product::getName() const
    -> const ::std::string&
{
    return m_name;
}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
auto ::pos::Product::getPrice() const
    -> float
{
    return m_price;
}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
auto ::pos::Product::getQuantity() const
    -> ::std::size_t
{
    return m_quantity;
}
